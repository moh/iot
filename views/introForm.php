        <h1>Qu'est-ce que l'IOT</h1>
        <div class="introParagText">
            <p>
                L’<strong>IoT</strong> est l’acronyme de <strong>Internet Of Things</strong> (Internet des Objets en français).
                 Le terme IoT est apparu la première fois en 1999 dans un discours de Kevin ASHTON, un ingénieur britannique.
                Il servait à désigner un système où les objets physiques sont connectés à Internet. 
                Il s’agit également de systèmes capables de créer et transmettre des données afin de 
                créer de la valeur pour ses utilisateurs à travers divers services (agrégation, analytique…).
            </p>

            <p>
                Selon l’UIT (Union Internationale des Télécommunications), l’Internet des Objets est défini comme <em>« une infrastructure 
                mondiale pour la société de l’information, qui permet de disposer de services évolués en interconnectant des objets 
                (physique ou virtuels) grâce aux technologies de l’information et de la communication interopérables existantes ou 
                en évolution »</em>.
            </p>
            <p>
                Au fil du temps, le terme a évolué et il englobe maintenant tout l’écosystème des objets connectés. Cet écosystème englobe,
                 des fabricants de capteurs, des éditeurs de logiciels, des opérateurs historiques ou nouveaux sur le marché, des intégrateurs… 
                 Cet éclectisme en fait sa richesse.
            </p>
        </div>
    </div>

    <div class="introParagraph">
        <h1>Les 5 composantes de l'IoT</h1>
        <div class="introParagText">
            <p>
                Une solution d’IoT s’articule autour de 5 composants essentiels que sont :
            </p>

            <ul>
                <li>Les objets (capteurs),</li>
                <li>Le réseau (connectivité),</li>
                <li>Les données,</li>
                <li>Les informations,</li>
                <li>Les applications d’exploitation.</li>
            </ul>

        </div>
    </div>

