        <h1>Quelques exemples d’application IoT</h1>
        <div class="introParagText">
            <p>
            l’<strong>IoT</strong> contribue à l’<strong>optimisation</strong> ou à la création de nouveaux usages pour les entreprises.
            Certains secteurs d’activité présentent un fort potentiel de développement :
            </p>
            <ul>
                <li>
                    <strong>L’Industrie</strong> avec la maintenance prédictive, la gestion automatisée d’alerte,
                    l’optimisation de processus, l’amélioration des rendements et des approvisionnements…
                </li>
                <li>
                    Les <strong>villes</strong> avec la gestion des stationnements, optimisation des flux de circulation,
                    la gestion de l’éclairage public, la gestion des consommations énergétiques…
                </li>
                <li>
                    Le <strong>Transport</strong> avec l’optimisation de flux de passager, l’optimisation des tournées de livraison…
                </li>
                <li>
                    Le <strong>Retail</strong> avec la réduction de la fraude, l’optimisation d’inventaire, la gestion de stock automatisée…
                </li>

